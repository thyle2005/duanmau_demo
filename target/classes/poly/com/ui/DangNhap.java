    public class AuthenticationManager {

        private static String loggedInUser;

        public static String getLoggedInUser() {
            return loggedInUser;
        }

        public static void setLoggedInUser(String username) {
            loggedInUser = username;
        }
    }

    private void login() {
        String user = txtMaNV.getText();
        String password = new String(txtMatKhau.getPassword());

        try (Connection con = DriverManager.getConnection(url, this.user, this.password)) {
            String sql = "SELECT * FROM NhanVien WHERE MaNV = ? AND MatKhau = ?";
            try (PreparedStatement pst = con.prepareStatement(sql)) {
                pst.setString(1, user);
                pst.setString(2, password);
                try (ResultSet rs = pst.executeQuery()) {
                    if (rs.next()) {
                        AuthenticationManager.setLoggedInUser(user);
                        JOptionPane.showMessageDialog(this, "Đăng nhập thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        EduSysJFrame main = new EduSysJFrame();
                        main.setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Đăng nhập thất bại. Tên đăng nhập hoặc mật khẩu không đúng.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối đến cơ sở dữ liệu: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
